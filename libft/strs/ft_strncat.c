#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	char	*s1;

	s1 = dest;
	while (*s1)
		s1++;
	while (*src && n-- > 0)
		*s1++ = *src++;
	*s1 = '\0';
	return (dest);
}
