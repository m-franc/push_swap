#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	char	*s1;

	s1 = dest;
	while (*s1)
		s1++;
	while (*src != '\0')
		*s1++ = *src++;
	*s1 = '\0';
	return (dest);
}
